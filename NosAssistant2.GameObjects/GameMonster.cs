	public static Image? GetIcon(int id)
	{
		if (!File.Exists($"images/npcs/{id}.png"))
		{
			if (!File.Exists("images/npcs/empty.png"))
			{
				return null;
			}
			return Image.FromFile("images/npcs/empty.png");
		}
		return Image.FromFile($"images/npcs/{id}.png");
	}

	public static Point? FindClosestSpawnPoint(int x, int y, int mob_id)
	{
		if (!SpawnTimedMobsFromID.ContainsKey(mob_id))
		{
			return null;
		}
		List<double> list = new List<double>();
		List<Point> possible_spawn_coordinates = SpawnTimedMobsFromID[mob_id].possible_spawn_coordinates;
		foreach (Point item in possible_spawn_coordinates)
		{
			list.Add(Utils.CalculateDistance(new Point(x, y), item));
		}
		return possible_spawn_coordinates[list.IndexOf(list.Min())];
	}

