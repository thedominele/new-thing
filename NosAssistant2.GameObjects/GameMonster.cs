	public static List<string> getMobNamesContainingSubstring(string substring)
	{
		return (from x in monsterNames.Values
			where x.ToLower().Contains(substring.ToLower()) && !isNPC(monsterNames.FirstOrDefault<KeyValuePair<int, string>>((KeyValuePair<int, string> y) => y.Value == x).Key)
			orderby x
			select x).ToList();
	}

	public static List<string> getNPCNamesContainingSubstring(string substring)
	{
		return (from x in monsterNames.Values
			where x.ToLower().Contains(substring.ToLower()) && isNPC(monsterNames.FirstOrDefault<KeyValuePair<int, string>>((KeyValuePair<int, string> y) => y.Value == x).Key)
			orderby x
			select x).ToList();
	}

	public static string GetNameById(int mobId)
	{
		if (!monsterNames.TryGetValue(mobId, out string value))
		{
			return "-";
		}
		return value;
	}

	public static int GetIdByName(string name)
	{
		KeyValuePair<int, string> keyValuePair = monsterNames.FirstOrDefault<KeyValuePair<int, string>>((KeyValuePair<int, string> kvp) => kvp.Value == name);
		if (!keyValuePair.Equals(default(KeyValuePair<int, string>)))
		{
			return keyValuePair.Key;
		}
		return -1;
	}

	private static bool isNPC(int id)
	{
		return npcs_ids.Contains(id);
